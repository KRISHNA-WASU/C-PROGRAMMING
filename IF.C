// C Program to demonstrate the syntax of if statement
#include <stdio.h>

int main()
{

	int a = 25 ;
	// if statement with true condition
	if (a < 30) {
		printf("%d is less than 30", a);
	}

	// if statement with false condition
	if (a > 40) {
		printf("%d is greater than 40", a);
	}

	return 0;
}
