// C program to illustrate the arithmatic operators
#include <stdio.h>

int main()
{

    int a = 25, b = 5;

    // using operators and printing results
    printf("a + b = %d\n", a + b);// (25 + 5) 
    printf("a - b = %d\n", a - b);// (25 - 5 )
    printf("a * b = %d\n", a * b);// (25 * 5 )
    printf("a / b = %d\n", a / b);// (25 / 5)
    printf("a %% b = %d\n", a % b);// (25 % 5 )
    printf("+a = %d\n", +a); // (+25)
    printf("-a = %d\n", -a); // (-25)
    printf("a++ = %d\n", a++); 
    printf("a-- = %d\n", a--);

    return 0;
}

