// Programm  for relational operator
#include <stdio.h>

int main() {
    int a,b;
    a=25;
    b=5 ;
    (a>b);//a is greater than b
    printf("%d\n",a>b);
    (a<b);//b is greater than a
    printf("%d\n",a<b);
    (a==b);//a is equal to b
    printf("%d\n",a==b);
    (a>=b);//a is greater than equal to b
    printf("%d\n",a>=b);
    (a<=b);//b is greater than equal to a
    printf("%d\n",a<=b);
    (a!=b);//a is not equal to b
    printf("%d\n",a!=b);
    
    return 0;
}
